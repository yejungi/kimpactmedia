let currentKbLang = 'ko';

function applyKbTranslations(lang) {
  const dict = kbTranslations[lang];
  if (!dict) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      // kb-nav-arrow 스팬이 있으면 보존
      const arrowSpan = el.querySelector('.kb-nav-arrow');
      if (arrowSpan) {
        // 텍스트 노드만 교체 (화살표 스팬 유지)
        const arrowHTML = arrowSpan.outerHTML;
        el.innerHTML = dict[key] + ' ' + arrowHTML;
      } else {
        el.textContent = dict[key];
      }
    }
  });

  // RTL support for Arabic
  if (lang === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.setAttribute('lang', 'ar');
    document.body.style.fontFamily = "'Noto Sans Arabic', sans-serif";
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.setAttribute('lang', lang);
    document.body.style.fontFamily = '';
  }

  // Update dropdown label
  const LANG_LABELS_KB = { ko: 'KO', en: 'EN', ja: 'JA', zh: 'ZH', ar: 'AR' };
  const kbLangLabel = document.getElementById('kb-lang-label');
  if (kbLangLabel) kbLangLabel.textContent = LANG_LABELS_KB[lang] || lang.toUpperCase();
  // Also update toggle button text directly
  const kbLangToggleBtn = document.getElementById('kb-lang-toggle');
  if (kbLangToggleBtn) {
    const labelSpan = kbLangToggleBtn.querySelector('span') || kbLangToggleBtn;
    labelSpan.textContent = LANG_LABELS_KB[lang] || lang.toUpperCase();
  }

  // Update active language button
  document.querySelectorAll('[data-lang]').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  currentKbLang = lang;
  localStorage.setItem('kb-lang', lang);
}

/* ============================================================
 * Init
 * ============================================================ */
document.addEventListener('DOMContentLoaded', () => {
  /* 드롭다운 토글 */
  const kbLangDropdown = document.getElementById('kb-lang-dropdown');
  const kbLangToggle = document.getElementById('kb-lang-toggle');
  if (kbLangToggle && kbLangDropdown) {
    // 드롭다운 컨테이너 전체에서 이벤트 버블링 차단
    kbLangDropdown.addEventListener('click', (e) => {
      e.stopPropagation();
    });
    kbLangToggle.addEventListener('click', (e) => {
      e.stopPropagation();
      kbLangDropdown.classList.toggle('open');
      kbLangToggle.setAttribute('aria-expanded', kbLangDropdown.classList.contains('open'));
    });
    document.addEventListener('click', () => {
      kbLangDropdown.classList.remove('open');
      if (kbLangToggle) kbLangToggle.setAttribute('aria-expanded', 'false');
    });
  }

  /* 드롭다운 옵션 클릭 */
  document.querySelectorAll('.kb-lang-opt:not([disabled])').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      e.preventDefault();
      const lang = btn.getAttribute('data-lang');
      applyKbTranslations(lang);
      if (kbLangDropdown) {
        kbLangDropdown.classList.remove('open');
        if (kbLangToggle) kbLangToggle.setAttribute('aria-expanded', 'false');
      }
    });
  });

  /* 구형 호환: .kb-lang-btn 클래스 */
  document.querySelectorAll('.kb-lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      applyKbTranslations(btn.getAttribute('data-lang'));
    });
  });

  const saved = localStorage.getItem('kb-lang') || 'ko';
  applyKbTranslations(saved);
});
